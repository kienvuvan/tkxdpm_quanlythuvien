    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        switch (sendRegisteUser()) {
            case MysqlUserDao.RESULT_NULL_POINT:
                JOptionPane.showMessageDialog(this, "Bạn cần điền đầy đủ các trường");
                break;
            case MysqlUserDao.RESULT_EMAIL_INCORRECT:
                JOptionPane.showMessageDialog(this, "Email không hợp lệ");
                break;
            case MysqlUserDao.RESULT_PHONE_INCORRECT:
                JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ");
                break;
            case MysqlUserDao.RESULT_PASS_SMALL:
                JOptionPane.showMessageDialog(this, "Mật khẩu quá ngắn");
                break;
            case MysqlUserDao.RESULT_ID_EXITS:
                JOptionPane.showMessageDialog(this, "Tài khoản đã tồn tại");
                break;
            case MysqlUserDao.RESULT_NO_CHECK_TERM:
                JOptionPane.showMessageDialog(this, "Bạn cần tích đọc rõ nội quy");
                break;
            case MysqlUserDao.RESULT_SUCCESS:
                resetField();
                JOptionPane.showMessageDialog(this, "Gửi đăng ký thành công,bạn phải chờ kích hoạt thẻ để có thể mượn sách");
                break;
            case MysqlUserDao.RESULT_SQLITE:
                JOptionPane.showMessageDialog(this, "Đã có lỗi xảy ra, mời đăng ký lại sau", "Thông báo", JOptionPane.ERROR_MESSAGE);
                break;
        }


    }//GEN-LAST:event_jButton1ActionPerformed

