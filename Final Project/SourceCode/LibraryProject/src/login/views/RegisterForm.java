    public int sendRegisteUser() {
        boolean check = checkReadTerm.isSelected();
        String id = usernameField.getText().trim();
        String name = nameField.getText().trim();
        String sex = sexGroup.getSelection().getActionCommand();
        String phone = phoneField.getText().trim();
        String type = typeGroup.getSelection().getActionCommand();
        String period = null;
        //  String id, String name, String studentId, String sex, String period, String email, String phone, String pass
        if (type.equals(Constants.STUDENT)) {
            period = periodComboField.getSelectedItem().toString();
        }
        String email = emailField.getText().trim();
        String studentId = idStudentField.getText().trim();

        return userController.insertUser(check, id, name, studentId, sex, period, email, phone, passField.getPassword());

    }

    private void resetField() {
        usernameField.setText("");
        nameField.setText("");
        passField.setText("");
        phoneField.setText("");
        emailField.setText("");
        idStudentField.setText("");
    }
